#!/usr/bin/env python3
"""This module define a concat function
"""


def concat(str1: str, str2: str) -> str:
    """Joins two strings together

    Arguments:
    =========
    str1 (string) - first string
    str2 (string) - second string

    Returns: (string) result of joining str1 and str2
    """
    return str1 + str2
